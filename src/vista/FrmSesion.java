    private void inicioSesion() {
        String usurio = txtUser.getText();
        String clave = new String(txtPass.getPassword());
        Sesion.setCuenta(cs.inicioSesion(usurio, clave));
        if (Sesion.getCuenta() != null) {
            if (Sesion.getCuenta().getEstado()) {
                Sesion.cargarDatos();
                JOptionPane.showMessageDialog(this, "BIENVENIDO: " + Sesion.getPersona().toString() + "\n "
                        + "Ha iniciado como: " + Sesion.getRol().toString());
                new Frm_Hospital().setVisible(true);
                this.dispose();
            } else {
                JOptionPane.showMessageDialog(null, "Cuenta no activa", "Error", JOptionPane.ERROR_MESSAGE);
            }

        } else {
            JOptionPane.showMessageDialog(null, "Usuario no encontrado", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }

